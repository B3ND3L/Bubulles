
public class Bulle {

    private double x;
    private double y;
    private double z;

    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    public double getZ(){
        return z;
    }

    public double setX(double x){
        this.x = x;
    }
    public double setY(double y){
        this.y = y;
    }
    public double setZ(double z){
        this.z = z;
    }
}
